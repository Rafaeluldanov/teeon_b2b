def merge_product(p_raw: dict, p_enrich: dict, parent_slug: str, idx: int) -> dict:
    """Merge a raw product with enrichment overrides."""
    out = dict(p_raw)
    # Always strip "(копия)" from titles for cleanliness
    if out.get('title', '').endswith('(копия)'):
        out['title'] = out['title'].replace(' (копия)', '').strip()
    # Apply enrichment
    for k, v in p_enrich.items():
        if k == 'description':
            out['description'] = v
        elif k == 'characteristics':
            out['characteristics'] = v
        elif k == 'tags':
            out['tags'] = v
        elif k in ('color', 'material', 'quantity'):
            if not out.get(k, '').strip():
                out[k] = v
        else:
            out[k] = v
    # Ensure characteristics is a list
    out.setdefault('characteristics', [])
    return out


def apply_enrich(case: dict) -> dict:
    """Apply enrichment dict to a case."""
    slug = case['slug']
    enrich = ENRICH.get(slug, {})
    out = dict(case)
    # slug override
    if 'slug_override' in enrich:
        out['slug'] = enrich['slug_override']
    # scalar/list fields
    for k in ['title', 'shortTitle', 'clientType', 'industry', 'year',
              'quantity', 'timeline', 'task', 'result', 'description',
              'products', 'technologies', 'coverLabel', 'galleryLabels',
              'seoTitle', 'seoDescription', 'tags', 'relatedCatalog',
              'relatedBranding']:
        if k in enrich:
            out[k] = enrich[k]
    # caseProducts: merge per-index enrichment
    if '_products' in enrich:
        new_products = []
        for i, p in enumerate(case.get('caseProducts', [])):
            p_enrich = enrich['_products'].get(i, {})
            new_products.append(merge_product(p, p_enrich, slug, i))
        out['caseProducts'] = new_products
    # ensure isActive
    if 'isActive' not in out:
        out['isActive'] = True
    return out


