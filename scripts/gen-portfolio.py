    lines.append(f"    galleryLabels: {ts_str_array(c.get('galleryLabels', []))},")
    lines.append(f"    seoTitle: {ts_str(c.get('seoTitle', ''))},")
    lines.append(f"    seoDescription: {ts_str(c.get('seoDescription', ''))},")
    lines.append(f"    tags: {ts_str_array(c.get('tags', []))},")
    lines.append(f"    relatedCatalog: {ts_str_array(c.get('relatedCatalog', []))},")
    lines.append(f"    relatedBranding: {ts_str_array(c.get('relatedBranding', []))},")
    lines.append(f"    isActive: {'true' if c.get('isActive', True) else 'false'},")
    if c.get('coverImage'):
        lines.append(f"    coverImage: {ts_str(c['coverImage'])},")
    if c.get('galleryImages'):
        lines.append(f"    galleryImages: [")
        for u in c['galleryImages']:
            lines.append(f"      {ts_str(u)},")
        lines.append(f"    ],")
