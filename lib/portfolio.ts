export const portfolioCaseMap: Record<string, PortfolioCase> = Object.fromEntries(
  portfolioCases.map((c) => [c.slug, c])
);

export function getPortfolioCase(slug: string): PortfolioCase | undefined {
  return portfolioCaseMap[slug];
}

export function getRelatedPortfolioCases(currentSlug: string, limit = 3): PortfolioCase[] {
  const current = portfolioCaseMap[currentSlug];
  if (!current) return portfolioCases.slice(0, limit);

  return portfolioCases
    .filter((c) => c.slug !== currentSlug)
    .map((c) => ({
      case: c,
      score: c.tags.filter((t) => current.tags.includes(t)).length,
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((x) => x.case);
}

export function getPortfolioCasesByTag(tag: string): PortfolioCase[] {
  return portfolioCases.filter((c) => c.tags.includes(tag));
}

export function getAllPortfolioTags(): string[] {
  return Array.from(new Set(portfolioCases.flatMap((c) => c.tags))).sort();
}
