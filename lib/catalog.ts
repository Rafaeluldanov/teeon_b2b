export function getCategoryBySlug(slug: string): CatalogCategory | undefined {
  return catalogCategories.find((c) => c.slug === slug);
}

export function getRelatedCategories(slugs: string[]): CatalogCategory[] {
  return slugs
    .map((s) => catalogCategories.find((c) => c.slug === s))
    .filter(Boolean) as CatalogCategory[];
}
