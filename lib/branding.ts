export function getBrandingMethod(slug: string): BrandingMethod | undefined {
  return brandingMethods.find((m) => m.slug === slug);
}

export function getRelatedBrandingMethods(slugs: string[]): BrandingMethod[] {
  return slugs
    .map((s) => brandingMethods.find((m) => m.slug === s))
    .filter(Boolean) as BrandingMethod[];
}
